// EXPORT Y EXPORT DEFAULT
/**
 * Export: Exporta elementos 
 * (funciones, variables, clases, etc.) 
 * de un archivo para que otros módulos 
 * puedan usarlos. Con export, puedes exportar
 * múltiples elementos de un archivo.
 */

export function sumar(a: number, b: number): number {
    return a + b;
}

export function restar(a: number, b: number): number {
    return a - b;
}
